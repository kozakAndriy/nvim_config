local forbiddenLogs = {
  'EGL',
  'MESA',
  'FrameTracker',
  'isarworker',
  'WindowOnBackDispatcher',
  'InsetsController',
  'ImeTracker',
  'InputConnectionAdaptor',
  'InputMethodManager',
  'Codec',
  'D/',
}

local function logFilter(str)
  if nil == str then
    return false
  end
  for i = 1, #forbiddenLogs do
    if string.find(str, forbiddenLogs[i]) then
      return false
    end
  end
  return not string.find(str, 'EGL')
    and not string.find(str, 'MESA')
    and not string.find(str, 'FrameTracker')
    and not string.find(str, 'isarworker')
    and not string.find(str, 'WindowOnBackDispatcher')
end

